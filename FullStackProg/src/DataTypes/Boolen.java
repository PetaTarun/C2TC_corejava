package DataTypes;

public class Boolen { //true or false is called boolen'

	public static void main(String[] args) {
		int a = 10;
		int b = 12;
		System.out.println(a > b);  //f
		System.out.println(a < b);  //t
		System.out.println(a == b); //f
		System.out.println(a <= b); //t
		System.out.println(a >=b);  //f
		System.out.println(a!=b);  //t
        }

}
